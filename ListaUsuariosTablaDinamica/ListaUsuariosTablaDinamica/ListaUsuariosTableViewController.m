- (NSInteger)numberOfSectionsInTableView:(UITableView *)tableView {
    return 1;
}

- (NSInteger)tableView:(UITableView *)tableView numberOfRowsInSection:(NSInteger)section {
    return [appDelegate.listaUsuarios count];
}


- (UITableViewCell *)tableView:(UITableView *)tableView cellForRowAtIndexPath:(NSIndexPath *)indexPath {
    UITableViewCell *cell = [tableView dequeueReusableCellWithIdentifier:@"idListaUsuarios" forIndexPath:indexPath];
    
    // Configure the cell...
    UILabel *nombre = (UILabel *)[cell viewWithTag:1001];
    nombre.text = [appDelegate.listaUsuarios objectAtIndex:indexPath.row ];
    
    return cell;
}


